import Foundation
import UIKit

extension UIColor{
    static var backgroundBlack: UIColor{
        return UIColor.white
    }
    
    static var backgroundGray: UIColor{
        return UIColor.darkGray
    }
    
    static var backgroundRed: UIColor{
        return UIColor.red
    }
    
    static var labelColor: UIColor{
        return UIColor.black
    }
}
